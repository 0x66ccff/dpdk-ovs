/**
 * A 'result' variable is used commonly throughout the OVDK unit test suite
 * to ensure that function calls within a test execute successfully. The
 * variable may be set numerous times during a test function run; however,
 * once set to a valid value (typically 0), it is never reset.
 *
 * Ensure that valid values are actually being returned by each function call
 * by resetting it to a negative value between calls.
 */
#define RESET(result)          \
	do {                   \
		*result = -1;  \
	}while(0)
